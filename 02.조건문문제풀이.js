// 문제
// KOI 전자에서는 건강에 좋고 맛있는 훈제오리구이 요리를 간편하게 만드는 인공지능 오븐을 개발하려고 한다.
// 인공지능 오븐을 사용하는 방법은 적당한 양의 오리 훈제 재료를 인공지능 오븐에 넣으면 된다.
// 그러면 인공지능 오븐은 오븐구이가 끝나는 시간을 분 단위로 자동적으로 계산한다.
// 또한, KOI 전자의 인공지능 오븐 앞면에는 사용자에게 훈제오리구이 요리가 끝나는 시각을 알려 주는 디지털 시계가 있다.
// 훈제오리구이를 시작하는 시각과 오븐구이를 하는 데 필요한 시간이 분단위로 주어졌을 때,
// 오븐구이가 끝나는 시각을 계산하는 프로그램을 작성하시오.

// 입력
// 첫째 줄에는 현재 시각이 나온다. 현재 시각은 시 A (0 ≤ A ≤ 23) 와 분 B (0 ≤ B ≤ 59)가 정수로 빈칸을 사이에 두고 순서대로 주어진다.
// 두 번째 줄에는 요리하는 데 필요한 시간 C(0 ≤ C ≤ 1, 000)가 분 단위로 주어진다.

// 출력
// 첫째 줄에 종료되는 시각의 시와 분을 공백을 사이에 두고 출력한다.
// (단, 시는 0부터 23까지의 정수, 분은 0부터 59까지의 정수이다.디지털 시계는 23시 59분에서 1분이 지나면 0시 0분이 된다.)

// 핵심 아이디어
// 1. 입력으로 주어진 시각을 '분'의 형태로 바꾸기
// 2. '분'이 주어졌을 때, 시각을 알려주는 기능을 작성하기

// 예시
// 입력 17시 40분 + 80분 = 1060분 + 80분 = 1140분
// 1. 1140를 1440(하루)으로 나눈 나머지를 취하기: 1140분
// 2. 이때, 60으로 나눈 몫을 시(hour)로 취하기: 19시
// 3. 이때, 60으로 나눈 나머지를 분(minute)으로 취하기: 0분

// 풀이
let fs = require("fs");
let input3 = fs.readFileSync("/dev/stdin").toString().split("\n");
let [a, b] = input3[0].split(" ").map(Number);
let c = Number(input3[1]);

let totalMinute = a * 60 + b + c; // 분의 형태로 바꾸기
totalMinute %= 1440;
let hour = parseInt(totalMinute / 60);
let minute = totalMinute % 60;

console.log(hour + " " + minute);
